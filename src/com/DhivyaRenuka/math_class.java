package com.DhivyaRenuka;

public class math_class {
    public static void main(String args[]){
        int num1=4, num2=7;
        System.out.println(Math.max(4,7));
        System.out.println(Math.max(num1, num2));
        System.out.println(Math.min(num1,num2));
        System.out.println(Math.sqrt(81));
        System.out.println(Math.abs(-81)); // change the _ve number to positive
        System.out.println(Math.random()); // give number b/w 0-1 can give 0 but not 1
        System.out.println(Math.random()); // give number b/w 0-1 can give 0 but not 1
        System.out.println(4+(8-4)*Math.random()); // give number b/w 4-8 can give 4 but not 8
        System.out.println(4+(8-4)*Math.random());
        System.out.println(4+(8-4)*Math.random());
        System.out.println(4+(8-4)*Math.random());
    }
}
